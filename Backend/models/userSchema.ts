import { User } from "../interfaces/user";
import mongoose from "mongoose";


const UserSchema = new mongoose.Schema<User>({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    isAdmin: {
        type: Boolean,
        default: false
    }
})

export const UserModel = mongoose.model('Users', UserSchema);